# 2. 극한의 필터링이 적용된 점 찾기 함수
# ==========================================
def get_top_2_centers(mask, is_hinge, img_width):
    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    valid_centers = []

    for c in contours:
        area = cv2.contourArea(c)

        if 1 < area < 500:
            M = cv2.moments(c)
            if M["m00"] != 0:
                cx = int(M["m10"] / M["m00"])
                cy = int(M["m01"] / M["m00"])

                if is_hinge and cx < img_width * 0.5:
                    continue

                valid_centers.append((cx, cy, area))

    valid_centers = sorted(valid_centers, key=lambda x: x[2], reverse=True)[:2]
    centers = [(x[0], x[1]) for x in valid_centers]
    return sorted(centers, key=lambda p: p[1]) if len(centers) == 2 else []

# ==========================================
# 3. 데이터 추출 실행
# ==========================================
success_count = 0

for pair in data_pairs:
    colored_dir = pair["colored"]
    raw_dir = pair["raw"]

    if not os.path.exists(colored_dir): continue

    for filename in os.listdir(colored_dir):
        if not filename.lower().endswith((".jpg", ".png")): continue

        file_num_str = filename.split('_')[-1].split('.')[0]
        if file_num_str.isdigit() and int(file_num_str) >= 21:
            curr_img_dir, curr_lbl_dir = val_img_dir, val_label_dir
        else:
            curr_img_dir, curr_lbl_dir = train_img_dir, train_label_dir

        colored_path = os.path.join(colored_dir, filename)
        raw_path = os.path.join(raw_dir, filename)

        if not os.path.exists(raw_path): continue

        img_array = np.fromfile(colored_path, np.uint8)
        image = cv2.imdecode(img_array, cv2.IMREAD_COLOR)
        if image is None: continue

        h, w, _ = image.shape
        hsv = cv2.cvtColor(image, cv2.COLOR_BGR2HSV)

        mask_green = cv2.inRange(hsv, np.array([50, 40, 200]), np.array([70, 150, 255]))
        mask_red = cv2.bitwise_or(
            cv2.inRange(hsv, np.array([0, 100, 100]), np.array([10, 255, 255])),
            cv2.inRange(hsv, np.array([170, 100, 100]), np.array([180, 255, 255]))
        )

        green_centers = get_top_2_centers(mask_green, is_hinge=True, img_width=w)
        red_centers = get_top_2_centers(mask_red, is_hinge=False, img_width=w)

        if len(green_centers) == 2 and len(red_centers) == 2:
            all_pts = green_centers + red_centers

            xs, ys = [p[0] for p in all_pts], [p[1] for p in all_pts]
            x_min, x_max = min(xs), max(xs)
            y_min, y_max = min(ys), max(ys)

            cx, cy = ((x_min + x_max) / 2) / w, ((y_min + y_max) / 2) / h
            bw, bh = (x_max - x_min) / w, (y_max - y_min) / h

            kpts_str = " ".join([f"{pt[0]/w:.6f} {pt[1]/h:.6f} 2" for pt in all_pts])

            txt_path = os.path.join(curr_lbl_dir, os.path.splitext(filename)[0] + ".txt")
            with open(txt_path, 'w', encoding='utf-8') as f:
                f.write(f"0 {cx:.6f} {cy:.6f} {bw:.6f} {bh:.6f} {kpts_str}\n")

            shutil.copy(raw_path, os.path.join(curr_img_dir, filename))

            debug_img = image.copy()
            cv2.circle(debug_img, green_centers[0], 8, (0, 255, 0), -1)
            cv2.circle(debug_img, green_centers[1], 8, (0, 255, 0), -1)
            cv2.circle(debug_img, red_centers[0], 8, (0, 0, 255), -1)
            cv2.circle(debug_img, red_centers[1], 8, (0, 0, 255), -1)
            cv2.imwrite(os.path.join(debug_dir, f"CHECK_{filename}"), debug_img)

            success_count += 1
            print(f"정밀 매칭 성공: {filename}")
        else:
            print(f"매칭 실패: {filename} (초록 점 발견 개수: {len(green_centers)}개)")

print(f"\n총 {success_count}개의 완벽한 정답 데이터가 생성되었습니다.")


