# 6. YOLO-Pose 학습 시작
# ==========================================
model = YOLO('yolov8s-pose.pt')
project_dir = "./runs/box_measure_pose"

results = model.train(
    data=yaml_path,
    epochs=300,
    patience=50,
    imgsz=800,
    batch=8,
    device=0,
    project=project_dir,
    name='exp_pose_high_acc',
    plots=True
)

print("\nYOLO-Pose 고정밀 모델 학습이 완료되었습니다!")

# ==========================================
# 7. 테스트 (Inference)
# ==========================================
weight_files = glob.glob(f"{project_dir}/*/weights/best.pt")
if not weight_files:
    print("학습된 모델 가중치를 찾을 수 없습니다.")
else:
    latest_model_path = max(weight_files, key=os.path.getctime)
    model = YOLO(latest_model_path)

    output_dir = "./results_inference"
    os.makedirs(output_dir, exist_ok=True)

    REAL_HINGE_MM = 153.0

    def calculate_distance(p1, p2):
        return math.sqrt((p2[0] - p1[0])**2 + (p2[1] - p1[1])**2)

    def get_dynamic_depth_ratio(raw_mm):
        if raw_mm < 130: return 1.10
        elif raw_mm < 160: return 1.15
        elif raw_mm < 180: return 1.10
        else: return 1.05

    for filename in os.listdir(val_img_dir):
        if not filename.lower().endswith((".jpg", ".png")): continue
        img_path = os.path.join(val_img_dir, filename)

        results = model.predict(source=img_path, imgsz=800, conf=0.25)

        img_array = np.fromfile(img_path, np.uint8)
        image = cv2.imdecode(img_array, cv2.IMREAD_COLOR)

        if results[0].keypoints is not None and len(results[0].keypoints.xy) > 0:
            candidates = results[0].keypoints.xy.cpu().numpy()
            chosen_kpts = None

            for kpts in candidates:
                if len(kpts) == 4:
                    box_top = kpts[2]
                    box_bottom = kpts[3]

                    x_dist = abs(box_bottom[0] - box_top[0])
                    y_dist = abs(box_bottom[1] - box_top[1])

                    if y_dist > x_dist and y_dist > 20:
                        chosen_kpts = kpts
                        break

            if chosen_kpts is not None:
                hinge_top = (int(chosen_kpts[0][0]), int(chosen_kpts[0][1]))
                hinge_bottom = (int(chosen_kpts[1][0]), int(chosen_kpts[1][1]))
                box_top = (int(chosen_kpts[2][0]), int(chosen_kpts[2][1]))
                box_bottom = (int(chosen_kpts[3][0]), int(chosen_kpts[3][1]))

                green_px_dist = calculate_distance(hinge_top, hinge_bottom)
                red_px_dist = calculate_distance(box_top, box_bottom)

                scale_factor = REAL_HINGE_MM / green_px_dist if green_px_dist > 0 else 0
                raw_box_mm = red_px_dist * scale_factor
                real_box_mm = raw_box_mm * get_dynamic_depth_ratio(raw_box_mm)

                cv2.line(image, hinge_top, hinge_bottom, (0, 255, 0), 2)
                cv2.line(image, box_top, box_bottom, (0, 0, 255), 2)

                print(f"[{filename}] 산출된 타겟: {real_box_mm:.1f} mm")
            else:
                print(f"[{filename}] 무시.")
        else:
             print(f"[{filename}] 인식 실패.")

        is_success, result_img_array = cv2.imencode(".jpg", image)
        if is_success:
            result_img_array.tofile(os.path.join(output_dir, f"AI_measured_{filename}"))