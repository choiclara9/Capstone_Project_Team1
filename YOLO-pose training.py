# 기본 데이터셋 경로 (로컬 폴더 기준)
dataset_base = "./yolo-pose-dataset"

data_pairs = [
    {"colored": f"{dataset_base}/colored/BOX1", "raw": f"{dataset_base}/raw/BOX_1"},
    {"colored": f"{dataset_base}/colored/BOX2", "raw": f"{dataset_base}/raw/BOX_2"},
    {"colored": f"{dataset_base}/colored/BOX3", "raw": f"{dataset_base}/raw/BOX_3"},
    {"colored": f"{dataset_base}/colored/BOX5", "raw": f"{dataset_base}/raw/BOX_5"},
    {"colored": f"{dataset_base}/colored/BOX6", "raw": f"{dataset_base}/raw/BOX_6"}
]

base_dataset_dir = "./datasets_pose"
train_img_dir = os.path.join(base_dataset_dir, "images", "train")
val_img_dir = os.path.join(base_dataset_dir, "images", "val")
train_label_dir = os.path.join(base_dataset_dir, "labels", "train")
val_label_dir = os.path.join(base_dataset_dir, "labels", "val")
debug_dir = "./debug_extraction"

for path in [train_img_dir, val_img_dir, train_label_dir, val_label_dir, debug_dir]:
    if os.path.exists(path): shutil.rmtree(path)
